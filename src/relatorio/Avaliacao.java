package relatorio;

public class Avaliacao {
	private String nome;
	private float nota;
	private float peso;	
	
	public String getNomeAvaliacao() {
		return nome;
	}
	public void setNomeAvaliacao(String nome) {
		this.nome = nome;
	}
	public float getNota() {
		return nota;
	}
	public void setNota(float nota) {
		this.nota = nota;
	}
	public float getPeso() {
		return peso;
	}
	public void setPeso(float peso) {
		this.peso = peso;
	}
}
